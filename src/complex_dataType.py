# list, tuple, dictionary, frozenset, set

our_list = ["a","b", "c", 1.5]

print(our_list)

our_tuple = ("a", "b", "c")
print(our_tuple)

our_dictionary = {"name":"json","height":"6.0"}
print(our_dictionary)

our_set = {"one", "two", "three"}
print(our_set)

